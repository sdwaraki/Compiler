
public class SymbolTable 
{
	String type;
	String scope;
	int value;
	
	public void setType(String t)
	{
		this.type = t;
	}
	
	public void setScope(String s)
	{
		this.scope = s;
	}
	
	public void setValue(int v)
	{
		this.value = v;
	}
	
	public String getType()
	{
		return this.type;
	}
	
	public String getScope()
	{
		return this.scope;
	}
	
	public int getValue()
	{
		return value;
	}
	
	
	
	

}
